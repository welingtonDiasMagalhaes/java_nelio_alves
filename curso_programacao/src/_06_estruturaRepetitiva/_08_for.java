package _06_estruturaRepetitiva;

public class _08_for {

	public static void main(String[] args) {

		System.out.println("for crescente:");
		for (int i = 0; i < 5; i++) {
			System.out.println("Valor de i: " + i);
		}

		System.out.println("for regressivo:");
		for (int i = 4; i >= 0; i--) {
			System.out.println("Valor de i: " + i);
		}

	}

}
